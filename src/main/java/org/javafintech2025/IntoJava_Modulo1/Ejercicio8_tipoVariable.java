package org.javafintech2025.IntoJava_Modulo1;

/*
Ejercicio 1
Si variableA, variableB y variableC son de tipo lógicas o boolean, ¿qué valores deben poseer para que las
siguientes operaciones den los siguientes resultados?:
● (variableA && variableB) || (variableA && variableC) = true
● !(variableA || variableB) && variableC = false
*/
public class Ejercicio8_tipoVariable {
    public static void main(String[] args) {
        boolean variableA = true;
        boolean variableB = false;
        boolean variableC = true;

        System.out.println("La variableA es true");
        System.out.println("La variableB es false");
        System.out.println("La variableC es true");
    }
}
